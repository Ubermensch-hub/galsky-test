#pragma once

#include <cstdint>

namespace core {

/* Задача для работы в системе с кооперативным планированием
 * Реализация на общем стеке вызовов внутри цикла планировщика
 * Обеспечивает детерминированность процессов с низкими накладными расходами
 * Текущее время подаёт планировщик: задачи не читают часы платформы сами,
 * поэтому тестируются детерминированно, без реального времени
*/
class ITask {
public:
    virtual ~ITask() = default;
    virtual void tick(uint32_t now_ms) = 0;
};

} // namespace core
